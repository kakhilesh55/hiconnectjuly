                  1. Not working on iPhone


                  </button>
                </h3>
                <div id="faq-content-5" class="accordion-collapse collapse" data-bs-parent="#faqlist">
                  <div class="accordion-body">
               1. Make sure their phone works with Hi Connect 
         </p>
The phone model you have does not matter when sharing!



<br>         
2. Make sure to touch your Hi Connect device to the very top of the back of your phone.
</p>
On iPhones, the sweet spot for reading a Hi Connect device is on the very top of the back of the device. 


<br>
3. A Hi Connect device CANNOT be read when:
</p>  
- their phone is in airplane mode
<br>
- their device flashlight is on
<br>
- their phone screen is off
<br>
- their camera is open 

